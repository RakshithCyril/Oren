                            6. Intended to be repaired rather than discarded.
                        </p>
                        <p class="funders">However, green marketing can easily turn into greenwashing and it
                            can happen when acompany does not adhere to the principles of sustainable business
                            practices, mislead and deceivetheir consumers by labels such as"eco-friendly,"
                            "organic," "natural," “cruelty free”and "green."</p>
                        <p class="funders">Here are a fewexamples of corporategreenwashing:
                            <br>
                            1. Green computing
                            <br>
                            Latest technologies, in general, and specifically information technology sector, are
                            major environmental allies. Since they allow us to do our work remotely and trendy
                            “work from home”, they say they are helping in reduction of carbon dioxide gas emission. As a result, many tech firms proclaim technology to be the planet's holy grail, which is a bit of greenwashing and this is because most of the components used in the electronic devices are made of rare Earth minerals, whose extraction (through mining activities) is extremely harmful to the environment.

                            <br>
                            2. Zero-emission vehicles
                            <br>
                            In terms of greenhouse gas emissions, nearly every automobile company
                            acknowledges the environmental courage of its electric, hybrid, or highly
                            fuel-efficient vehicle models. These companies do "forget" to report the
                            (rarely green) source of the energy used to recharge their much celebrated
                            vehicle’s batteries, there are several complexities in recycling the environmentally
                            damaging lithium-ion batteries used in the cars, and there are even cheating on
                            emission tests and other "improved" data and figures, so in terms of reliability
                            the situation could be a little skeptical.
                            <br>
                            3. Non-stick cookware
                            <br>
                            There is hardly any delicacy which can be cooked without oil.However, if you switch to
